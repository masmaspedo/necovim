local quote = require'necovim.plugins.quotes'

local random_quote = function()
    if #quote == 0 then
        return "" -- Return nil if quote is empty
    end
    math.randomseed(os.time()) -- Seed for randomness
    return quote[math.random(1, #quote)]
end

